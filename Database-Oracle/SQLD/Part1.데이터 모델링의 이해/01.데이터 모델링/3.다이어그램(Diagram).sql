E-R(Entity-Relationship) : 개체 간 관계를 정의하고 표현하는데 매우 유용한 도구 

- 개체(Entity) 타입 : 사각형
- 관계(Relationship) 타입 : 마름모
- 속성(attribute) : 작대기 원
- 키 속성 : 작대기 원+(안에작대기)
- 약한개체타입 : 2중 사각형 

✅ 관계와 연결은 다르다. 모델링에서 연결이라는 단어는 사용되지 않는다.

